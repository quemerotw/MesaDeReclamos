package mesa;

public class ReclamoSoftware {
	private String nombre;
	private String sistemaOperativo;
	private String observaciónFalla;
	
	
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	public String getSistemaOperativo() {
		return sistemaOperativo;
	}
	public void setSistemaOperativo(String sistemaOperativo) {
		this.sistemaOperativo = sistemaOperativo;
	}
	public String getObservaciónFalla() {
		return observaciónFalla;
	}
	public void setObservaciónFalla(String observaciónFalla) {
		this.observaciónFalla = observaciónFalla;
	}
	
	
}
